        if(editarContrasenaCB.isSelected()){
            contrasenaT.setEnabled(true);
            rContrasenaT.setEnabled(true);
        }
        else{
           contrasenaT.setEnabled(false);
            rContrasenaT.setEnabled(false); 
        }
        
