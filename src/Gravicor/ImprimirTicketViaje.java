    public ImprimirTicketViaje(String queryViajes,
    String[] columnasViajes,
    String IDcamion,
    String tipoCamion,
    String operadorCamion,
    String numeroDeViajes) {
        initComponents();
        boolean  bandera = Globales.baseDatos.insertarEnTabla( queryViajes,columnasViajes, tabla);
        camionLB.setText(IDcamion);
        tipoLB.setText(tipoCamion);
        viajesTotalesLB.setText(numeroDeViajes);
        operadorLB.setText(operadorCamion);
        
        Integer mCubicos = new Integer(numeroDeViajes);
        Integer toneladas = new Integer(numeroDeViajes);
        
        if(tipoCamion.equals(new String("rabon"))){
            mCubicos *= 6;
            toneladas *= 11;
        }
        else{
            mCubicos *= 12;
            toneladas *= 22;
        }
        
        cantidadTotalLB.setText(mCubicos.toString());
        toneladasTotalesLB.setText(toneladas.toString());
    }

