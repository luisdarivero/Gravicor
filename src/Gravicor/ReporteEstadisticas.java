    private int esBisiesto(int anio){
        int flag = 0;
        int loEs = 0;
        
        if(anio%400 == 0)
            flag = 1;
        else if(anio%4 == 0 && anio%100 != 0)
            flag = 1;
        else
            flag = 0;
        
        if(flag == 1)
            loEs = 1;
        else
            loEs = 0;
        return loEs;
    }
    
